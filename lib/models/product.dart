//@dart=2.9
class Product {
  final String id;
  final String name;
  final String brand;
  final String image;
  final String description;
  final double price;
  final String category;
  final String model;
  final String sellerName;

  Product({
    this.id,
    this.name,
    this.brand,
    this.description,
    this.price,
    this.category,
    this.image,
    this.model,
    this.sellerName,
  });
}
